package bookred.main.dto;

public class StatisticsVO {

	private int cnt;
	private String day;
	public int getCnt() {
		return cnt;
	}
	public void setCnt(int cnt) {
		this.cnt = cnt;
	}
	public String getDay() {
		return day;
	}
	public void setDay(String day) {
		this.day = day;
	}
	@Override
	public String toString() {
		return "StatisticsVO [cnt=" + cnt + ", day=" + day + "]";
	}
	
	
	
}
