public class DriveThruVO {
	
	private int dt_seq;
	private String dt_car_no;
	private int dt_ispickup;
	private int rm_no;
	
	public int getDt_seq() {
		return dt_seq;
	}
	public void setDt_seq(int dt_seq) {
		this.dt_seq = dt_seq;
	}
	public String getDt_car_no() {
		return dt_car_no;
	}
	public void setDt_car_no(String dt_car_no) {
		this.dt_car_no = dt_car_no;
	}
	public int getDt_ispickup() {
		return dt_ispickup;
	}
	public void setDt_ispickup(int dt_ispickup) {
		this.dt_ispickup = dt_ispickup;
	}
	public int getRm_no() {
		return rm_no;
	}
	public void setRm_no(int rm_no) {
		this.rm_no = rm_no;
	}

}
