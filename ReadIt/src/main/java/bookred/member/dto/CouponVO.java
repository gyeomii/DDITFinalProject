package bookred.member.dto;

import java.util.Date;

public class CouponVO {
	private int cp_no;
	private String mem_id;
	private Date cp_issueday;
	private Date cp_expireday;
	private int cp_status;
	public int getCp_no() {
		return cp_no;
	}
	public void setCp_no(int cp_no) {
		this.cp_no = cp_no;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public Date getCp_issueday() {
		return cp_issueday;
	}
	public void setCp_issueday(Date cp_issueday) {
		this.cp_issueday = cp_issueday;
	}
	public Date getCp_expireday() {
		return cp_expireday;
	}
	public void setCp_expireday(Date cp_expireday) {
		this.cp_expireday = cp_expireday;
	}
	public int getCp_status() {
		return cp_status;
	}
	public void setCp_status(int cp_status) {
		this.cp_status = cp_status;
	}
	@Override
	public String toString() {
		return "CouponVO [cp_no=" + cp_no + ", mem_id=" + mem_id + ", cp_issueday=" + cp_issueday + ", cp_expireday="
				+ cp_expireday + ", cp_status=" + cp_status + "]";
	}
	
	
	

}
